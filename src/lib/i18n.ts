import { cookies } from 'next/headers';

export type Lang = 'pt' | 'en' | 'es';

export async function getLang(): Promise<Lang> {
  const store = await cookies();
  const lang = store.get('lang')?.value;
  return lang === 'en' || lang === 'es' ? lang : 'pt';
}

